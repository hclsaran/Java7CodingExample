package com.usk.training.day2;

public class RelationalOpeEx {

	public static void main(String[] args) {
		int a = 10;
		int b = 5;
		int c = 10;

		System.out.println(a == b);
		System.out.println(a != b);
		System.out.println(a == c);
		System.out.println(a > b);
		System.out.println(a < b);
		System.out.println(a >= c);

	}

}
